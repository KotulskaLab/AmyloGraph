#' @importFrom shiny reactive
#' @importFrom dplyr select `%>%`
#' @importFrom icecream ic
reactive_select_table <- function(edges) reactive({
  ic(edges[["table"]])
  edges[["table"]] %>% 
    mutate(doi = linkify_doi(doi)) %>% 
    select(AGID,
           interactor_name,
           interactee_name,
           aggregation_speed,
           elongates_by_attaching,
           heterogenous_fibers,
           doi)
})